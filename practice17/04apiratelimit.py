# 👉 Agent로 바꾸면:
# 식사 입력
#  → 분석
#  → 부족 영양 판단
#  → 추천 생성
#  → 알림 예약
# MyLog
# 글 작성 → 요약 → 스타일 변환

# 👉 Agent 구조:

# 글 입력
#  → 요약
#  → 태깅
#  → 스타일 변환
#  → 검증
