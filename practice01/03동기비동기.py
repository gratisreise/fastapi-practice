# 3. 동기 vs 비동기 (sync / async / await)
# 1️⃣ 동기 (Sync)

# 동기는 아주 단순하다.

# 하나 끝나야 다음 거 한다

# 흐름
# 요청 1 → 처리 완료 → 요청 2 → 처리 완료 → 요청 3
# 예시 코드
def work():
    db_call()   # 3초 걸림
    return "done"

# 👉 db_call() 끝날 때까지 아무것도 못함

# 문제 (핵심 포인트)
# 요청 1 → DB 3초
# 요청 2 → 기다림
# 요청 3 → 기다림

# 👉 CPU는 놀고 있는데도 기다림
# 👉 이게 병목

# 2️⃣ 비동기 (Async)

# 비동기는 이렇게 생각하면 된다.

# 기다리는 동안 다른 일 한다

# 흐름
# 요청 1 → DB 요청 (대기)
# 요청 2 처리
# 요청 3 처리
# → 요청 1 결과 돌아오면 처리

# 👉 동시에 여러 요청 처리 가능

# 핵심 키워드
# async → 비동기 함수 선언
# await → "여기서 기다리긴 하는데 다른 일 먼저 해"
# 예시 코드
async def work():
    await db_call()   # 기다리는 동안 다른 요청 처리 가능
    return "done"
# sync vs async 차이 핵심
# 구분	sync	async
# 처리 방식	순차	동시에
# 대기 방식	블로킹	논블로킹
# 성능	I/O에서 느림	I/O에서 빠름
# 코드 난이도	쉬움	약간 어려움
# 중요한 오해 (🔥 핵심)

# 비동기는

# ❌ "CPU가 빨라지는 것"
# ❌ "무조건 더 빠른 것"

# 👉 아니다

